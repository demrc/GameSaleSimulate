package Entity;

public class Discount {
	private double discountRate;

	public Discount(double discountRate) {
		super();
		this.discountRate = discountRate;
	}

	public double getDiscountRate() {
		return discountRate;
	}

	public void setDiscountRate(double discountRate) {
		this.discountRate = discountRate;
	}
	

}
